package array.array;
// Declarando uma array:
public class ArrayExemplo5 {
    public static void main(String[] args) {
        // Quando já tenho as informações e o tamanho do array:
        int[] idades = {1, 2, 3, 4, 5};
        for (int contador = 0; contador < idades.length; contador++) { // Para fazer a ligação o
            System.out.println("Na posição " +  + contador + " temos o número: "+ idades[contador]);



        }
    }
}
